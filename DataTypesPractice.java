public class DataTypesPractice {
    public static void main(String[] args) {
        int a = 10, b = 40;
        System.out.println("Addition of a and b: " + a + " + " +  b  + " = " + (a+b));
        System.out.println("Subtraction of a and b: " + a + " - " +  b  + " = " + (a-b));
        System.out.println("Multiplication of a and b: " + a + " * " +  b  + " = " + (a*b));
        System.out.println("Division of a and b: " + a + " / " +  b  + " = " + (a/b));
    }
}
